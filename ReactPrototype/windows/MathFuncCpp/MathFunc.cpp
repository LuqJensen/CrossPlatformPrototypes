#include "pch.h"
#include "MathFunc.h"

using namespace MathFuncCpp;
using namespace Platform;

MathFunc::MathFunc()
{

}

double MathFunc::add(double a, double b)
{
	return a + b;
}

double MathFunc::subtract(double a, double b)
{
	return a - b;
}

double MathFunc::multiply(double a, double b)
{
	return a * b;
}

double MathFunc::divide(double a, double b)
{
	return a / b;
}
